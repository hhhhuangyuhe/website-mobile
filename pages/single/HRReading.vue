    },
    onConfirm(value) {
      this.hrreading.scare = value;
      this.showPicker = false;
    },
    GoApply() {
      if (
        this.hrreading.name == "" ||
        this.hrreading.position == "" ||
        this.hrreading.phone == "" ||
        this.hrreading.email == "" ||
        this.hrreading.company == "" ||
        this.hrreading.scare == ""
      ) {
        this.$toast("请填写完整信息");
        return;
      }
      const params = {
        model: JSON.stringify({
          ID: 0,
          CompanyName: this.hrreading.company,
          CompanyRange: this.hrreading.scale,
          LinkName: this.hrreading.name,
          LinkPhone: this.hrreading.phone,
          Email: this.hrreading.email,
          Position: this.hrreading.position,
          AddressRemark: this.hrreading.address,
          FunnyContent: '',
          Type: "服务",
          CreateDate: new Date(),
          IsDelete: 0,
        }),
      };
      this.$axios({
        method: "post",
        url: `/pc/Form/AddCustomerInfo`,
        data: qs.stringify(params),
        headers: {
          "Content-Type": "application/x-www-form-urlencoded; charset=UTF-8",
        },
      }).then((res) => {
        console.log(res.data == false);
        if ((res.data).toLowerCase() == 'true' || res.data === true) {
          this.$dialog.alert({
            message: "申请成功！",
          });
        } else {
          this.$dialog.alert({
            message: "服务繁忙",
          });
        }
      });
      this.showApply = false;
    },
  },
};
</script>

